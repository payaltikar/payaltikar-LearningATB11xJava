package ex_03_Literal;

public class Lab02_Literal_p1 {
    public static void main(String[] args) {
        // Literal
        int age = 65;
        age = 66; //we can change the value of variable many times
        System.out.println(age);

        // Constant
        final int girl_age = 18;
        //girl_age = 19;
        System.out.println(girl_age);// we can not able to change value of constant variable
    }
}
